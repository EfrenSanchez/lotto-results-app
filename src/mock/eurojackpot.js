// (:String) -> Array <{ last:euroJackpot }>
export function getEuroJackpotDrawingByDate(dateString) {
  const indexOfDrawing = euroJackpot
    .map(
      ({ date }) =>
        `${date.year}${atLeastTwoFigures(date.month)}${atLeastTwoFigures(
          date.day
        )}`
    )
    .indexOf(dateString);

  const last = euroJackpot[indexOfDrawing] || {};
  return [{ last }];
}

// () -> { last: euroJackpot }
export function getEuroJackpotLastDrawing() {
  const [last] = euroJackpot;

  return { last };
}
